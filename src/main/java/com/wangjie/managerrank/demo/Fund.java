package com.wangjie.managerrank.demo;

public class Fund {
    private String code;
    private String name;
    private String type;
    private String start_date;
    private String end_date;
    private String duty_days;
    private String duty_return;
    private String average;
    private String rank;
    private String manager;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getStart_date() {
        return start_date;
    }

    public void setStart_date(String start_date) {
        this.start_date = start_date;
    }

    public String getEnd_date() {
        return end_date;
    }

    public void setEnd_date(String end_date) {
        this.end_date = end_date;
    }

    public String getDuty_days() {
        return duty_days;
    }

    public void setDuty_days(String duty_days) {
        this.duty_days = duty_days;
    }

    public String getDuty_return() {
        return duty_return;
    }

    public void setDuty_return(String duty_return) {
        this.duty_return = duty_return;
    }

    public String getAverage() {
        return average;
    }

    public void setAverage(String average) {
        this.average = average;
    }

    public String getRank() {
        return rank;
    }

    public void setRank(String rank) {
        this.rank = rank;
    }

    public String getManager() {
        return manager;
    }

    public void setManager(String manager) {
        this.manager = manager;
    }
}
